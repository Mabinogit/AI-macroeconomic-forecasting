# Data Plan for Inflation Rate Prediction

## Target Variable
- Consumer Price Index (CPI) / Inflation rate

## Data Source
- World Bank
- URL: https://data.worldbank.org/indicator/FP.CPI.TOTL

## Frequency
- Quarterly (or yearly if quarterly is incomplete)

## Scope
- Start with one country (e.g., United States)
- Expand to additional countries later

## Storage
- Raw CSV stored in `data/raw/`
- Cleaned CSV stored in `data/processed/`
